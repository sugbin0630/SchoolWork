public class Comment {
	public static void main(String[] args) {
		String Tip; // Make sure your variable name is proper name
		Tip = "all variable have to be on top of the code"; // put in-line comment in here
      
		// use newline to divide code
		System.out.println(Tip + "\n");
		// two slash for comment
		/* 
		 * slash + * for start block comment + slash for end block comment 
		 */
		System.out.println("use header block for write name, date, tip, opreation of code");
		// also we can do like this
		/*==================
      	I         H        I   
      	I         e        I
      	I         l        I
      	I         l        I
      	I         o        I
      	==================*/
	}
}
